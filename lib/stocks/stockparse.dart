import 'package:stockview/stocks/stock.dart';

class StockParse {
  final List<Stock> stocks = [];

  // 解析
  void parse(final String data) {
    stocks.clear();

    List<String> dataList = data.split(";");
    for (int i = 0; i < dataList.length; i++) {
      String s = dataList[i].trim();
      if (s.isNotEmpty) {
        stocks.add(Stock(s));
      }
    }
  }
}
